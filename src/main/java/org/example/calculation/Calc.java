package org.example.calculation;

public class Calc {

    public static int secti(int a, int b){
        return a+b;
    }

    public static int vydel(int a, int b){
        return a/b;
    }

    public static int vynasob(int a, int b){
        return a*b;
    }

    public static int odecti(int a, int b){
        return b-a;
    }


}

